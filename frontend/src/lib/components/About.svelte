  <h2 class="text-xl font-bold text-gray-900 dark:text-white">About HAMSTR</h2>
  <p>HAMSTR is an open-source project enabling decentralized NOSTR communication over ham radio.
    This system allows off-grid clients to interact with NOSTR relays through a server with internet access,
    utilizing HF Ham Radio Packets.</p>
